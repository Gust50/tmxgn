#import "TMXHomeModelDetailModel.h"

@implementation TMXHomeModelDetailModel

+(NSDictionary *)mj_replacedKeyFromPropertyName
{
    return @{@"detailID":@"id"};
}

+(NSDictionary *)mj_objectClassInArray
{
    return @{@"stlFileList":[TMXHomeModelDetailFileListModel class], @"modelCommentList":[TMXHomeModelDetailCommentMainListModel class]};
}

-(void)FetchTMXHomeModelDetailModel:(NSDictionary *)params callBack:(void (^)(BOOL, id))completion
{
    NSString *Url=[NSString stringWithFormat:@"%@%@",URL_Header,TMX_ModelDetail];
    
    [[KBHttpTool shareKBHttpToolManager]post:Url params:params success:^(id response) {
        
        BaseModel *baseModel=[BaseModel mj_objectWithKeyValues:response];
        if (baseModel.code==ResponseSuccess) {
            TMXHomeModelDetailModel *model = [TMXHomeModelDetailModel mj_objectWithKeyValues:baseModel.content];
            completion(YES,model);
        }else{
            //错误信息
            completion(NO,baseModel.message);
        }
    } failure:^(NSError *error) {
        completion(NO,ServerError);
    }];
}

@end

